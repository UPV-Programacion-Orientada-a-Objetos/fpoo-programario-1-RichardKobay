package edu.upvictoria.fpoo.helpers;

public class Conversions {
    public double galsToLiters(double gal) {
        return gal * 3.785;
    }

    public double metersToInches(double m) {
        return m * 0.0254;
    }

    public double pesosToDollars(double pesos) {
        return pesos * 0.058;
    }
}
